<template>
    <div class="mx-auto main-container">
        <v-card class="mx-auto filters">
            <v-form
                    @submit.prevent="searchFlats"
                    lazy-validation
                    ref="flatsFilters"
            >
                <v-row justify="space-between">
                    <v-col class="d-flex" cols="12" sm="3">
                        <v-select :items="countFlats"
                                  :value="activeCountFlatsId"
                                  @input="setActiveCountFlatsId"
                                  countFlat
                                  item-text="name"
                                  item-value="id"
                                  label="Количество комнат"
                        ></v-select>
                    </v-col>

                    <v-col class="d-flex" cols="12" sm="3">
                        <v-select :items="districts"
                                  :value="activeDistrictId"
                                  @input="setActiveDistrictId"
                                  district
                                  item-text="name"
                                  item-value="id"
                                  label="Район"
                        ></v-select>
                    </v-col>

                    <v-col class="d-flex" cols="12" sm="3">
                        <v-select :items="developers"
                                  :value="activeDeveloperId"
                                  @input="setActiveDeveloperId"
                                  developer
                                  item-text="name"
                                  item-value="id"
                                  label="Застройщик"
                        ></v-select>
                    </v-col>

                    <v-col class="d-flex" cols="12" sm="3">
                        <v-select :items="wallMaterials"
                                  :value="activeWallMaterialId"
                                  @input="setActiveWallMaterialId"
                                  item-text="name"
                                  item-value="id"
                                  label="Материал стен"
                                  wallMeterial
                        ></v-select>
                    </v-col>
                </v-row>

                <v-row justify="space-between">
                    <v-col class="d-flex" cols="12" sm="4">
                        <v-row>
                            <v-col class="d-flex" cols="1" sm="6">
                                <v-text-field
                                        @input="setCostMin"
                                        countFrom
                                        dense
                                        :rules="[value => {if(!costMax || value <= costMax) return
                                        else {return 'Минимальная стоимость должна быть меньше максимальной.'}}]"
                                        label="От"
                                        type="number"
                                        outlined
                                        v-model.number="costMin"
                                ></v-text-field>
                            </v-col>
                            <v-col class="align-center" cols="1" sm="6">
                                <v-text-field
                                        @input="setCostMax"
                                        countTo
                                        dense
                                        :rules="[value => {if(!costMin || value >= costMin) return
                                        else {return  'Максимальная стоимость должна быть больше минимальной.'}}]"
                                        type="number"
                                        label="До"
                                        outlined
                                        v-model.number="costMax"
                                ></v-text-field>
                            </v-col>
                        </v-row>
                    </v-col>

                    <v-col class="d-flex" cols="12" sm="4">
                        <v-row>
                            <v-col class="d-flex" cols="1" sm="6">
                                <v-text-field
                                        @input="setSquareMin"
                                        dense
                                        label="От"
                                        :rules="[value => {if(!squareMax || value <= squareMax) return
                                        else {return 'Минимальная площадь должна быть меньше максимальной.'}}]"
                                        type="number"
                                        outlined
                                        squareMin
                                        v-model.number="squareMin"
                                ></v-text-field>
                            </v-col>
                            <v-col class="d-flex" cols="1" sm="6">
                                <v-text-field
                                        @input="setSquareMax"
                                        dense
                                        label="До"
                                        :rules="[value => {if(!squareMin || value >= squareMin) return
                                        else {return  'Максимальная площадь должна быть больше минимальной.'}}]"
                                        type="number"
                                        outlined
                                        squareMax
                                        v-model.number="squareMax"
                                ></v-text-field>
                            </v-col>
                        </v-row>
                    </v-col>
                    <v-col class="d-flex" cols="12" sm="4">
                        <v-btn class="ma-2 justify-end"
                               color="success"
                               type="submit"
                        >
                            Показать
                        </v-btn>
                    </v-col>
                </v-row>

                <v-row
                        align="center"
                        justify="start"
                >
                    <v-col :key="filter"
                           class="shrink"
                           v-for="(filter,index) in currentFilters"
                    >
                        <v-chip @click:close="removeFilter({filter: filter, index: index})"
                                close
                        >
                            {{ filter }}
                        </v-chip>
                    </v-col>
                </v-row>
            </v-form>
        </v-card>

        <v-divider class="divider"></v-divider>
        <v-container class="grey lighten-5">
            <v-row :key="i"
                   justify="start"
                   v-for="i in Math.ceil(flats.length/3)"
            >
                <v-col :key="i*3+j"
                       md="4"
                       v-for="j in flats.length - ((i-1)*3)"
                >
                    <FlatCard :image="flats[(i-1)*3+j-1].photo"
                              :key="flats[(i-1)*3+j-1].id"
                              :message="text(flats[(i-1)*3+j-1])"
                              :title="flats[(i-1)*3+j-1].address">
                    </FlatCard>
                </v-col>
            </v-row>
        </v-container>
    </div>
</template>

<script>
    import FlatCard from "./FlatCard";
    import {mapActions, mapGetters, mapMutations} from 'vuex'

    export default {
        name: "Flats",
        components: {FlatCard},

        data() {
            return {}
        },

        validations: {
            costMax: {},
            costMin: {},
            squareMax: {},
            squareMin: {}
        },

        methods: {
            ...mapMutations([
                'setActiveCountFlatsId',
                'setActiveDistrictId',
                'setActiveDeveloperId',
                'setCostMin',
                'setCostMax',
                'setSquareMin',
                'setSquareMax',
                'setActiveWallMaterialId',
            ]),

            ...mapActions([
                'getFlats',
                'getCountFlats',
                'getDistricts',
                'getDevelopers',
                'getWallMaterials',
                'removeFilter',
            ]),

            text(flat) {
                return `${flat.description}\n${flat.flat_type}\n${flat.realty_type}`
            },

            searchFlats() {
                this.$refs.flatsFilters.validate();
                this.getFlats();
            },
        },

        computed: {
            ...mapGetters([
                'flats',
                'countFlats',
                'activeCountFlatsId',
                'districts',
                'activeDistrictId',
                'developers',
                'activeDeveloperId',
                'wallMaterials',
                'activeWallMaterialId',
                'costMin',
                'costMax',
                'squareMin',
                'squareMax',
                'currentFilters',
            ]),

        },

        mounted() {
            this.getCountFlats();
            this.getDistricts();
            this.getDevelopers();
            this.getWallMaterials();
            this.getFlats();
        }
    }
</script>

